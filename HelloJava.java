package LEARN_JAVA;

public class HelloJava {

	public static void main(String[] args) {
		
		// output the text and break the line
		System.out.println("( println ): Hello Java");
		
		//output the text and continuing on the same line
		System.out.print("( print ): Hello Java.");
		
		// without breaking the line
		System.out.print("See the difference?");
	}

}
